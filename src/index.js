const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0'
};

function decode(expr) {
    let words = [];
    while (expr) {
        words.push(expr.slice(0, 10));
        expr = expr.slice(10);
    }
    for (let word of words) {
        let letter = word.replace(/00/g, '').replace(/10/g, '.').replace(/11/g, '-');
        words.splice(words.indexOf(word), 1, MORSE_TABLE[letter] || ' ');
    }
    return words.join('');
}

module.exports = {
    decode
}